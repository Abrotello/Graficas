public class Coordenadas {
    //Rectangulo
    private int[] puntoA = {50, 200};
    private int[] puntoB = {630, 200};
    private int[] puntoC = {50, 500};
    private int[] puntoD = {630, 500};

    //Circulo
    private int radio = 60;

    public Coordenadas() {}

    public int[] getPuntoA() {
        return puntoA;
    }

    public int[] getPuntoB() {
        return puntoB;
    }

    public int[] getPuntoC() {
        return puntoC;
    }

    public int[] getPuntoD() {
        return puntoD;
    }

    public int getRadio() {
        return radio;
    }
}
